#include <stdio.h>

// Quick Sort Implementation
void swap(int* a, int* b) {
    int temp = *a;
    *a = *b;
    *b = temp;
}

int partition(int arr[], int low, int high) {
    int pivot = arr[high];
    int i = (low - 1);
    for (int j = low; j < high; j++) {
        if (arr[j] < pivot) {
            i++;
            swap(&arr[i], &arr[j]);
        }
    }
    swap(&arr[i + 1], &arr[high]);
    return (i + 1);
}

void quickSort(int arr[], int low, int high) {
    if (low < high) {
        int pi = partition(arr, low, high);
        quickSort(arr, low, pi - 1);
        quickSort(arr, pi + 1, high);
    }
}

void printArray(int arr[], int size) {
    for (int i = 0; i < size; i++)
        printf("%d ", arr[i]);
    printf("\n");
}

// Merge Sort Implementation
void merge(int arr[], int l, int m, int r) {
    int i, j, k;
    int n1 = m - l + 1;
    int n2 = r - m;
    int L[n1], R[n2];
    for (i = 0; i < n1; i++)
        L[i] = arr[l + i];
    for (j = 0; j < n2; j++)
        R[j] = arr[m + 1 + j];
    i = 0, j = 0, k = l;
    while (i < n1 && j < n2) {
        if (L[i] <= R[j])
            arr[k++] = L[i++];
        else
            arr[k++] = R[j++];
    }
    while (i < n1)
        arr[k++] = L[i++];
    while (j < n2)
        arr[k++] = R[j++];
}

void mergeSort(int arr[], int l, int r) {
    if (l < r) {
        int m = l + (r - l) / 2;
        mergeSort(arr, l, m);
        mergeSort(arr, m + 1, r);
        merge(arr, l, m, r);
    }
}

// Radix Sort Implementation
int getMax(int arr[], int n) {
    int max = arr[0];
    for (int i = 1; i < n; i++)
        if (arr[i] > max)
            max = arr[i];
    return max;
}

void countSort(int arr[], int n, int exp) {
    int output[n];
    int count[10] = {0};
    for (int i = 0; i < n; i++)
        count[(arr[i] / exp) % 10]++;
    for (int i = 1; i < 10; i++)
        count[i] += count[i - 1];
    for (int i = n - 1; i >= 0; i--) {
        output[count[(arr[i] / exp) % 10] - 1] = arr[i];
        count[(arr[i] / exp) % 10]--;
    }
    for (int i = 0; i < n; i++)
        arr[i] = output[i];
}

void radixSort(int arr[], int n) {
    int m = getMax(arr, n);
    for (int exp = 1; m / exp > 0; exp *= 10)
        countSort(arr, n, exp);
}

int main() {
    printf("The given number is: \n75, 45, 40, 80, 35, 78, 98, 30, 39, 100\n");
    int quickArr[] = {75, 45, 40, 80, 35, 78, 98, 30, 39, 100};
    int mergeArr[] = {75, 45, 40, 80, 35, 78, 98, 30, 39, 100};
    int radixArr[] = {798, 89, 697, 8421, 9, 520};
    int quickSize = sizeof(quickArr) / sizeof(quickArr[0]);
    int mergeSize = sizeof(mergeArr) / sizeof(mergeArr[0]);
    int radixSize = sizeof(radixArr) / sizeof(radixArr[0]);
    
    printf("Quick Sort:\n");
    quickSort(quickArr, 0, quickSize - 1);
    printArray(quickArr, quickSize);
    
    printf("Merge Sort:\n");
    mergeSort(mergeArr, 0, mergeSize - 1);
    printArray(mergeArr, mergeSize);
    
    printf("Radix Sort:\n");
    radixSort(radixArr, radixSize);
    printArray(radixArr, radixSize);
    
    return 0;
}